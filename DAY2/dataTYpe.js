// DATA TYPES IN JAVASCRIPT

// -------- Numbers --------
let price = 49.75;
let quantity = 4;
console.log(price + quantity);   // Addition
console.log(price * quantity);   // Multiplication

// -------- Strings --------
const userFirstName = "Aman";
const userLastName = "Sharma";
const instituteName = "IIT Delhi";

console.log(userFirstName + " " + userLastName);
console.log(instituteName);

// -------- Booleans --------
let isUserLoggedIn = true;
console.log(isUserLoggedIn);

// -------- Null --------
let lastActiveUser = null; // intentional empty value
console.log(lastActiveUser);

// -------- Undefined --------
let lastSessionTime; // automatically undefined
console.log(lastSessionTime);